namespace BlazorWebFormsComponents;

/// <summary>
/// Specifies the HTTP method used when submitting a form.
/// Mirrors the ASP.NET Web Forms <c>FormMethod</c> enumeration.
/// </summary>
public enum FormMethod
{
	/// <summary>HTTP GET method.</summary>
	Get = 0,

	/// <summary>HTTP POST method.</summary>
	Post = 1
}
