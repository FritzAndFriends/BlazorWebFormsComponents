namespace BlazorWebFormsComponents.Enums
{
	/// <summary>
	/// Specifies the scope of a table header cell.
	/// </summary>
	public enum TableHeaderScope
	{
		/// <summary>
		/// The scope is not set.
		/// </summary>
		NotSet = 0,

		/// <summary>
		/// The header applies to a row.
		/// </summary>
		Row = 1,

		/// <summary>
		/// The header applies to a column.
		/// </summary>
		Column = 2
	}
}
