namespace BlazorWebFormsComponents.Enums
{
	/// <summary>
	/// Specifies the position of the pager in a data-bound control.
	/// </summary>
	public enum PagerPosition
	{
		/// <summary>
		/// Pager is displayed at the bottom of the control.
		/// </summary>
		Bottom = 0,

		/// <summary>
		/// Pager is displayed at the top of the control.
		/// </summary>
		Top = 1,

		/// <summary>
		/// Pager is displayed at both the top and bottom of the control.
		/// </summary>
		TopAndBottom = 2
	}
}
