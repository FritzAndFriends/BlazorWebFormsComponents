    private TextBox Code = default!;
    private Button CodeSubmit = default!;
    private PlaceHolder ErrorMessage = default!;
    private Literal FailureText = default!;
    private DropDownList<object> Providers = default!;
    private Button ProviderSubmit = default!;
    private CheckBox RememberBrowser = default!;
    private HiddenField SelectedProvider = default!;
    private PlaceHolder sendcode = default!;
    private PlaceHolder verifycode = default!;
    // --- ConfigurationManager Migration ---
    // TODO(bwfc-config): ConfigurationManager calls work via BWFC shim.
